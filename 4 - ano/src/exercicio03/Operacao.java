package exercicio03;

public class Operacao {
	
	public static double multiplicarNumero(double numeroEscolhido1, double numeroEscolhido2) {
		
		return numeroEscolhido1 * numeroEscolhido2;
	}	
	
	public static double somarNumero(double numeroEscolhido1, double numeroEscolhido2  ) {
		
		return  numeroEscolhido1 + numeroEscolhido2;
		
	}
	public static double subtrairNumero(double numeroEscolhido1, double numeroEscolhido2  ) {
		
		return  numeroEscolhido1 - numeroEscolhido2;
		
	}
	public static double dividirNumero(double numeroEscolhido1, double numeroEscolhido2  ) {
		
		return  numeroEscolhido1 / numeroEscolhido2;
		
	}
	
	
	
	
	
	
	
}
